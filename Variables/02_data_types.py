# Exercise: Basic Data Types

# Integer (whole number)
age = 25
print("Age:", age)
print("Type:", type(age))

# Float (decimal number)
height = 1.75
print("Height:", height)
print("Type:", type(height))

# String (text)
name = "Alice"
print("Name:", name)
print("Type:", type(name))

# Boolean (True or False)
is_student = True
print("Is student:", is_student)
print("Type:", type(is_student))
