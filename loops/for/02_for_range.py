# Exercise 02: For Loop with Range Variations

# Print numbers from 0 to 4
print("Range with one argument:")
for i in range(5):
    print(i)

# Print numbers from 2 to 6
print("\nRange with start and stop:")
for i in range(2, 7):
    print(i)

# Print even numbers from 0 to 10 (step = 2)
print("\nRange with start, stop, and step:")
for i in range(0, 11, 2):
    print(i)

# Print numbers in reverse from 5 to 1
print("\nRange in reverse:")
for i in range(5, 0, -1):
    print(i)
